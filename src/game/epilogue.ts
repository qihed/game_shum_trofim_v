import { story } from './story'
import type { Choice, GameHistoryEntry, StatKey, Stats } from './types'

export type EpilogueSection = {
  title: string
  body: string
}

export type Epilogue = {
  headline: string
  intro: string
  sections: EpilogueSection[]
}

const statLabels: Record<StatKey, string> = {
  talent: 'талант',
  craft: 'ремесло',
  watching: 'насмотренность',
  teamTrust: 'доверие команды',
  audienceContact: 'контакт со зрителем',
  projectViability: 'жизнеспособность проекта',
  staminaEnergy: 'выносливость',
}

function choiceNet(choice: Choice): number {
  return Object.values(choice.deltaStats ?? {}).reduce((s, v) => s + (v ?? 0), 0)
}

function getBlockChoice(blockId: number, choiceId: string): Choice | null {
  const block = story.blocks.find((b) => b.id === blockId)
  return block?.choices.find((c) => c.id === choiceId) ?? null
}

function getHistoryChoice(history: GameHistoryEntry[], blockId: number): Choice | null {
  const entry = history.find((h) => h.kind === 'block' && h.blockId === blockId)
  if (!entry || entry.kind !== 'block') return null
  return getBlockChoice(blockId, entry.choiceId)
}

function toneFromStats(stats: Stats): 'strong' | 'mixed' | 'fragile' {
  const avg =
    (stats.craft +
      stats.teamTrust +
      stats.audienceContact +
      stats.projectViability +
      stats.staminaEnergy) /
    5
  if (avg >= 7) return 'strong'
  if (avg <= 4) return 'fragile'
  return 'mixed'
}

function pathHighlights(history: GameHistoryEntry[]): string[] {
  const pivotal = [1, 3, 6, 8, 11, 13, 14]
  const lines: string[] = []

  for (const blockId of pivotal) {
    const entry = history.find((h) => h.kind === 'block' && h.blockId === blockId)
    if (!entry || entry.kind !== 'block') continue
    const block = story.blocks.find((b) => b.id === blockId)
    const choice = getBlockChoice(blockId, entry.choiceId)
    if (!block || !choice) continue

    const net = choiceNet(choice)
    const tone =
      net >= 3 ? 'сильный ход' : net <= -2 ? 'слабый ход' : 'осторожный, но неоднозначный ход'

    lines.push(
      `На этапе «${block.title}» вы сделали ${tone}: ${choice.label.toLowerCase()}. ${
        choice.consequence
          ? choice.consequence.split('.')[0] + '.'
          : 'Это повлияло на то, как дальше сложились съёмка, монтаж и отношения в команде.'
      }`,
    )
  }

  return lines
}

function projectArc(stats: Stats, history: GameHistoryEntry[]): string {
  const montage = getHistoryChoice(history, 11)
  const montageNet = montage ? choiceNet(montage) : 0
  const pitch = getHistoryChoice(history, 3)
  const pitchNet = pitch ? choiceNet(pitch) : 0
  const tone = toneFromStats(stats)

  const platform =
    stats.projectViability >= 7 && stats.craft >= 6
      ? 'Фильм «Без звука» вышел на платформу «Голос района» в укороченной, но цельной версии. Продюсерка поставила его в середину выпуска — не как главный, но и не в хвост.'
      : stats.projectViability >= 5
        ? '«Без звука» дошёл до сдачи, но с оговорками: часть сцен снята в спешке, монтаж несколько раз пересобирали. На платформе фильм появился тихо — без баннера, зато без срыва дедлайна.'
        : 'Проект едва дожил до финала. Сдали файл с опозданием на сутки, в заставке опечатка, одна сцена в библиотеке так и осталась «красивой, но лишней». На платформу фильм попал уже обрезанным и без анонса — скорее как галочка, чем как событие.'

  const craftNote =
    montageNet >= 3
      ? 'К финалу вы сумели убрать лишнее и оставить один нерв — честный голос героя. Команда вздохнула: «Наконец понятно, о чём фильм».'
      : montageNet <= -1
        ? 'Монтаж так и не собрался в одну линию: зритель видел куски сильных сцен, но между ними провисала объяснялка и лишние проходы по городу.'
        : 'Монтаж сделали «достаточно хорошим», чтобы не стыдно было показать. Герой в финале говорит правду, но до неё зрителю приходится долго ждать.'

  const pitchNote =
    pitchNet >= 3
      ? 'Ещё на питче вы заложили ясность: не «тишина ради тишины», а человек, который впервые записывает честное сообщение отцу. Это потом спасло съёмку, когда на площадке всё шло криво.'
      : pitchNet <= -1
        ? 'С самого питча в проекте висел туман: красивые слова про одиночество, но слабая механика сцены. Команда старалась, однако режиссёрский центр проявлялся поздно.'
        : 'Питч прошёл ровно — тему поняли, детали доуточняли по ходу. Фильм рос не из лозунга, а из проб на площадке.'

  const personal =
    tone === 'strong'
      ? 'Максим закончил проект уставшим, но с ощущением: «Я не доказал талант — я провёл работу». В портфолио появилась первая взрослая ссылка, которую не стыдно отправить с сопроводительным письмом.'
      : tone === 'fragile'
        ? 'Максим закончил проект с пустотой вместо гордости. Фильм есть, опыт есть, но внутренний голос шепчет: «Это мог сделать и случайный человек с хорошей камерой».'
        : 'Максим закончил проект со смешанным чувством. Где-то он вырос, где-то снова надеялся на атмосферу. Главное — он не бросил на полпути, а это для первого продакшена уже много.'

  return [platform, pitchNote, craftNote, personal].join('\n\n')
}

function audienceArc(stats: Stats, history: GameHistoryEntry[]): string {
  const show = getHistoryChoice(history, 13)
  const showNet = show ? choiceNet(show) : 0
  const teaser = getHistoryChoice(history, 4)
  const teaserNet = teaser ? choiceNet(teaser) : 0

  const screening =
    showNet >= 3
      ? 'После закрытого показа в кино-кафе вы не убежали от тишины. Вернулись в зал, задали вопросы, записали, где зритель потерял героя. Отец почти ничего не сказал, зато незнакомый парень из студсовета написал на следующий день: «Узнал себя в сцене у двери».'
      : showNet <= -1
        ? 'После показа зал вежливо замолчал. Максим объяснил тишину «неподготовленной аудиторией» — и разговор закрылся. В комментариях лаборатории появилось холодное: «Красиво, но далеко».'
        : 'Показ прошёл ровно: «интересно», «есть потенциал», «местами затянуто». Настоящей боли не было, но и восторга тоже. Максим понял: фильм не провалился громко — он просто не зацепил.'

  const feedbackTiming =
    stats.audienceContact >= 6
      ? 'Обратную связь Максим собрал не за один вечер, а за неделю: разговор в зале, сообщение наставника, три комментария в чате команды. Впервые критика стала не ударом, а картой правок.'
      : stats.audienceContact <= 4
        ? 'Обратную связь Максим получил обрывками и поздно. Наставник написал через два дня, зрители в зале почти не спросили, в сети фильм набрал скромные просмотры без споров. Он долго не понимал, то ли фильм слабый, то ли он просто не умеет слушать.'
        : 'Через сутки после показа пришли первые отклики: монтажер — про темп, актёр — про финал, наставник — про середину. Максим читал их на кухне и вдруг поймал себя на том, что не защищается — просто кивает.'

  const online =
    teaserNet >= 2
      ? 'Тизер вы заранее настроили честно: без ложного «бунта поколений». В сети собралась узкая, но своя аудитория — люди, которые ждали не манифест, а тихую правду.'
      : teaserNet <= -1
        ? 'Тизер обещал громче, чем фильм смог сказать. Часть зрителей пришла на показ ждать конфликта с родителями и ушла разочарованной — это тоже часть публичной реакции.'
        : 'В сети фильм жил тихо: несколько сотен просмотров, пара репостов от друзей команды, один разбор в студенческом паблике без злости и без восторга.'

  return [screening, feedbackTiming, online].join('\n\n')
}

function fatherArc(stats: Stats, finalId: string | undefined): string {
  const open = stats.audienceContact >= 6 && stats.craft >= 6

  if (finalId === 'А') {
    return `Отец пришёл на показ и ушёл, не дождавшись обсуждения. Дома он сказал только: «Ну, смотрелось неплохо. Ты всё ещё учишься?» Максим кивнул — и оба сделали вид, что вечер ничего не изменил. Кино в семье снова стало темой, которую лучше не поднимать.`
  }
  if (finalId === 'C' || finalId === 'D') {
    return open
      ? `После показа отец долго молчал в машине. Потом спросил: «Ты правда это снял?» — не с укоризной, а с усталостью. Когда Максим на коротком разборе с командой назвал слабые места, отец слушал у двери и в конце сказал: «Я не понимаю всё. Но вижу, что ты не врал в финале». Полного благословения не случилось. Но лед тронулся.`
      : `Отец смотрел фильм до титров и на обсуждении сказал: «Много воды». Максим не стал спорить. Вечером отец сам написал короткое: «Мама бы тоже не поняла. Но ты старался». Для их семьи это почти объятие.`
  }
  if (finalId === 'B') {
    return `Отец увидел праздничные сторис Максима и решил, что «всё получилось». На семейном ужине он хвалил «красивую картинку», не замечая, что сын внутри не рад. Максим улыбнулся — и впервые почувствовал, как одиночество бывает даже рядом с родителями.`
  }
  return `Отец остался фигурой за кадром: звонок в библиотечной сцене, ожидание «нормальной работы», молчание после показа. Максим ещё не решил, говорить ли с ним настоящим голосом или снова записать отредактированное видео.`
}

function spbArc(history: GameHistoryEntry[]): string {
  const spb = getHistoryChoice(history, 14)
  const spbNet = spb ? choiceNet(spb) : 0
  const finalEntry = history.findLast((h) => h.kind === 'final')
  const finalId = finalEntry?.kind === 'final' ? finalEntry.choiceId : undefined

  if (finalId === 'А') {
    return `Письмо из Петербурга так и осталось без ответа — или с вежливым «сейчас не готов». Переезд отложен не на месяц, а на неопределённость. Иногда Максим листает чат и думает, что, может, там уже нашли другого режиссёра. Это не конец, но дверь закрыта с его стороны.`
  }

  if (spbNet >= 4) {
    return `Максим попросил время, сделал разбор «Без звука» и только потом написал питерскому продюсеру: «Да, выйду режиссёром». Через две недели — короткий созвон, тестовое задание, билет на три дня «посмотреть площадку». Переезда пока нет: сначала один проект без иллюзий. Но впер впервые звучит как план, а не как мечта из Instagram.`
  }
  if (spbNet <= -2) {
    return `Максим согласился на питерский проект слишком рано — из страха, что больше не позовут. Уже на первой смене в чужом городе он ловит себя на старой ошибке: красивые кадры, слабая сцена, команда не понимает, кто принимает решения. Переезд обсуждают, но внутренний Максим знает: сначала надо научиться не сбегать вперёд себя.`
  }
  if (spbNet >= 1) {
    return `Питерский ролик Максим берёт параллельно с допилкой своего фильма. Сон уходит, зато в портфолио появляется второй клиент. О переезде говорят осторожно: «Если сработаемся — обсудим осень». Пока это не билет в новую жизнь, а пробный мост.`
  }
  return `Сообщение «Ты выйдешь режиссёром?» висит в переписке без ясного ответа. Петербург остаётся возможностью на горизонте — слишком дорогой, чтобы броситься, и слишком заманчивой, чтобы забыть.`
}

function finaleArc(finalId: string | undefined): string {
  switch (finalId) {
    case 'А':
      return `Вы выбрали уйти — сказать себе, что кино, наверное, не ваше. Максим закрывает рабочие чаты, возвращается к учёбе, иногда ночью открывает старый монтаж и сразу закрывает. Через полгода он снимает только stories для друзей. Талант никуда не делся — он просто перестал проходить через дисциплину и площадку. Иногда в этом тоже защита: не каждый обязан платить мечтой болью. Но если он вернётся, вернётся уже другим человеком — с памятью о том, каково это, когда проект зависит не от вдохновения, а от вас.`

    case 'B':
      return `Вы выбрали сохранить лицо — будто всё получилось. Снаружи путь выглядит ровно: показ, благодарности, кадры в ленте. Внутри Максим знает слабые места и не трогает их. Следующий проект он снова начнёт с красивого образа, потому что признать ошибку публично страшнее, чем повторить её. Команда верит в него — пока не увидит тот же финал второй раз.`

    case 'C':
      return `Вы выбрали честный разбор и маленький следующий шаг. Максим собирает команду, называет три слабых места без оправданий, предлагает через месяц снять одну сцену — две минуты, одна задача, без фестивальной истерики. Это не громкий прорыв. Это впервые взрослый ритм: не доказывать, а учиться. Лаборатория смотрит на него иначе — не как на «талантливого мальчика», а как на режиссёра, который умеет возвращаться к работе после тишины в зале.`

    case 'D':
      return `Вы выбрали публично принять опыт и идти дальше. Максим говорит в зале: «Фильм не везде дотянут, я вижу где». Куратор лаборатории предлагает подать новую заявку — не за прошлый фильм, а за следующий, уже с уроками. Это не гарантия славы. Это редкий момент, когда слабая работа не обнуляет автора, а открывает разговор. Максим уходит домой уставшим, но с адресом, куда нести энергию дальше.`

    default:
      return `Финальный выбор остался за кадром — но путь всё равно сложился в историю, которую можно разобрать по показателям и решениям на площадке.`
  }
}

function futureArc(stats: Stats, finalId: string | undefined): string {
  const t = toneFromStats(stats)
  const base =
    finalId === 'А'
      ? 'Ближайшие месяцы — юридические зачёты, редкие съёмки «для души», постепенное остывание к большим заявкам. Кино может вернуться — но только если он перестанет требовать от себя сразу чуда.'
      : finalId === 'B'
        ? 'Впереди ещё один «красивый» проект, ещё один тизер, ещё одна вежливая тишина после показа. Рост возможен, но он отложен — за стеной из «всё нормально».'
        : finalId === 'C'
          ? 'Впереди маленькие этюды, короткие сцены, возможно — вторая попытка в «Линии» уже без гонки за масштабом. Максим учится жить в ритме: неделя съёмки, неделя разбора, без подвигов каждые десять дней.'
          : finalId === 'D'
            ? 'Впереди заявка в лабораторию, новый сценарий на восемь минут, разговор с продюсером на равных. Он ещё будет ошибаться — но уже не будет притворяться, что ошибка — это злой зритель.'
            : 'Будущее пока открытое — как черновик без финального названия.'

  const statsLine =
    t === 'strong'
      ? `По итоговым показателям у вас сильнее всего ${topStats(stats).join(' и ')} — это опора, на которую можно ставить следующий проект, а не оправдание прошлого.`
      : t === 'fragile'
        ? `Показатели в конце (${topStats(stats, true).join(', ')}) напоминают: путь был тяжёлым. Следующий проект стоит брать меньше и честнее — иначе снова сольётся в хаос.`
        : `Баланс показателей смешанный — вы где-то выросли, где-то платили тревогой. Это нормальная карта первого взрослого опыта, не приговор.`

  return `${base}\n\n${statsLine}`
}

function topStats(stats: Stats, weakest = false): string[] {
  const entries = (Object.entries(stats) as [StatKey, number][]).sort((a, b) =>
    weakest ? a[1] - b[1] : b[1] - a[1],
  )
  return entries.slice(0, 2).map(([k]) => statLabels[k])
}

function pathSummary(history: GameHistoryEntry[], stats: Stats): string {
  const blockEntries = history.filter((h) => h.kind === 'block')
  let strong = 0
  let weak = 0
  for (const e of blockEntries) {
    if (e.kind !== 'block') continue
    const c = getBlockChoice(e.blockId, e.choiceId)
    if (!c) continue
    const net = choiceNet(c)
    if (net >= 3) strong++
    else if (net <= -2) weak++
  }

  const tone = toneFromStats(stats)
  return `За ${blockEntries.length} развилок вы ${strong} раз(а) выбрали решения, которые заметно усилили проект, и ${weak} раз(а) — те, что его ослабили. Общий тон пути — ${
    tone === 'strong'
      ? 'режиссёр, который учится держать и смысл, и площадку'
      : tone === 'fragile'
        ? 'автор с чувством, но без устойчивой системы'
        : 'человек между вдохновением и ремеслом, уже ближе ко второму'
  }. Ни один выбор не определяет судьбу целиком — но вместе они собрали фильм, показ, ответ отца и отношение к питерскому сообщению.`
}

export function buildEpilogue(stats: Stats, history: GameHistoryEntry[]): Epilogue {
  const finalEntry = history.findLast((h) => h.kind === 'final')
  const finalId = finalEntry?.kind === 'final' ? finalEntry.choiceId : undefined
  const finalChoice = finalId
    ? story.final?.choices.find((c) => c.id === finalId)
    : null

  const highlights = pathHighlights(history)

  return {
    headline: finalChoice?.label ?? 'Ваш путь',
    intro: `Это не отчёт с оценкой — развёрнутое резюме того, как сложилась жизнь проекта «Без звука» и что ждёт Максима после. Вы прошли историю от первого письма из продакшена до тишины в зале и последнего выбора на кухне. Ниже — разбор ваших решений, судьба фильма, реакция публики, отец, Петербург и будущее.`,
    sections: [
      {
        title: 'Разбор вашего пути',
        body: [pathSummary(history, stats), ...highlights].join('\n\n'),
      },
      {
        title: 'Что вышло с проектом',
        body: projectArc(stats, history),
      },
      {
        title: 'Публика и обратная связь',
        body: audienceArc(stats, history),
      },
      {
        title: 'Отец',
        body: fatherArc(stats, finalId),
      },
      {
        title: 'Петербург и переезд',
        body: spbArc(history),
      },
      {
        title: 'Ваш финальный выбор',
        body: finaleArc(finalId),
      },
      {
        title: 'Что ждёт дальше',
        body: futureArc(stats, finalId),
      },
    ],
  }
}
