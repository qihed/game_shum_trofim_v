function splitLabelAndText(beforeType) {
  const trimmed = beforeType.trim()
  const m = trimmed.match(/^(.+?)(?=Максим|Герой|Он\s|Отказываем)/)
  if (m && m[1].length < trimmed.length) {
    return { label: m[1].trim(), text: trimmed.slice(m[1].length).trim() }
  }
  return { label: trimmed, text: '' }
}

function applyChoiceType(cur, fragment) {
  const typeMatch = fragment.match(/Тип выбора:\s*(плохой|средний|успешный)/i)
  if (!typeMatch) return fragment.trim()
  const t = typeMatch[1].toLowerCase()
  cur.quality = t === 'плохой' ? 'bad' : t === 'успешный' ? 'good' : 'medium'
  return fragment.split(/Тип выбора:/i)[0].trim()
}

function parseDocxText(text) {
  const chunks = text.trim().split(/\n(?=\d+\.\s)/)
  /** @type {Array<{ title: string, question: string, situation: string, choices: Array<{ label: string, text: string, quality: 'bad'|'medium'|'good' }> }>} */
  const blocks = []

  for (const chunk of chunks) {
    const lines = chunk.split('\n').map((l) => l.trimEnd())
    if (!lines[0]?.match(/^\d+\.\s/)) continue

    const title = lines[0].replace(/^\d+\.\s+/, '').trim()
    let question = ''
    let situation = ''
    /** @type {Array<{ label: string, text: string, quality: 'bad'|'medium'|'good' }>} */
    const choices = []
    let i = 1

    if (lines[i]?.startsWith('Вопрос:')) {
      question = lines[i].replace(/^Вопрос:\s*/, '').trim()
      i++
    }

    if (lines[i]?.startsWith('История:')) {
      const parts = [lines[i].replace(/^История:\s*/, '').trim()]
      i++
      while (i < lines.length && !lines[i].startsWith('Выбор игрока:')) {
        if (lines[i]) parts.push(lines[i].trim())
        i++
      }
      situation = parts.filter(Boolean).join('\n\n')
    }

    if (lines[i]?.startsWith('Выбор игрока:')) i++

    let cur = null
    const flush = () => {
      if (!cur) return
      cur.text = cur.text.trim()
      choices.push(cur)
      cur = null
    }

    while (i < lines.length) {
      const line = lines[i]
      const choiceStart = line.match(/^([АБВ])\.\s*(.*)$/)
      if (choiceStart) {
        flush()
        let rest = choiceStart[2].trim()
        cur = { label: '', text: '', quality: 'medium' }
        if (/Тип выбора:/i.test(rest)) {
          const beforeType = applyChoiceType(cur, rest)
          const parts = splitLabelAndText(beforeType)
          cur.label = parts.label
          cur.text = parts.text
        } else {
          const parts = splitLabelAndText(rest)
          cur.label = parts.label
          cur.text = parts.text
        }
        i++
        continue
      }
      if (/^Показатели:/i.test(line) || /^Талант\s*\+/i.test(line)) {
        i++
        while (i < lines.length && !lines[i].match(/^[АБВ]\./) && !lines[i].match(/^\d+\.\s/)) {
          i++
        }
        continue
      }
      if (cur && line) {
        if (/Тип выбора:/i.test(line)) {
          const beforeType = applyChoiceType(cur, line)
          if (beforeType) cur.text += (cur.text ? ' ' : '') + beforeType
        } else if (!/^Тип выбора:/i.test(line)) {
          cur.text += (cur.text ? ' ' : '') + line.trim()
        }
      }
      i++
    }
    flush()

    const ordered = ['bad', 'medium', 'good'].map((q) => {
      const found = choices.find((c) => c.quality === q)
      if (!found) throw new Error(`Block "${title}": missing ${q} choice`)
      return found
    })

    blocks.push({ title, question, situation, choices: ordered })
  }

  if (blocks.length !== 14) {
    throw new Error(`Expected 14 blocks from docx, got ${blocks.length}: ${blocks.map((b) => b.title).join('; ')}`)
  }
  return blocks
}

function pack(block, triple, orderIdx) {
  const order = shuffleOrders[orderIdx]
  const choices = order.map((i, pos) => ({
    id: displayIds[pos],
    label: triple[i].label,
    text: triple[i].text,
    consequence: triple[i].consequence,
    analysis: '',
    reflectionQuestion: '',
    deltaStats: triple[i].deltaStats,
  }))
  return { ...block, choices }
}

function buildBlocks(parsed) {
  return parsed.map((b, idx) => {
    const triple = [0, 1, 2].map((qi) => ({
      label: b.choices[qi].label,
      text: b.choices[qi].text || b.choices[qi].label,
      consequence: blockConsequences[idx][qi],
      deltaStats: blockDeltas[idx][qi],
    }))
    return pack(
      {
        id: idx + 1,
        title: b.title,
        question: b.question,
        situation: b.situation,
      },
      triple,
      idx,
    )
  })
}

